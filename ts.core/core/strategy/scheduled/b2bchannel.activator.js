module.exports = function (options, callback, errorcallback) {
    var core = options.core;
    var started = new Date();
    console.log('Scheduled task started at ' + started);
    var common = require('yourttoo.common');
    var _ = require('underscore');
    var cev = common.eventtrigger.eventcarrier(common.utils.getToken());
    var totals = {
        activations: activation.length,
        activated: 0
    };

    cev.on('activation.done', function () { 
        callback({ ResultOK: true, Message: 'DMC B2B Channel activation finished', Total: totals });
    });

    cev.on('dmc.process', function (activation, dmc) {
        console.log('processing... ' + dmc.code);
        dmc.membership.b2bchannel = activation.membership.b2bchannel;
        dmc.membership.b2cchannel = activation.membership.b2cchannel;
        dmc.membership.b2bcommission = activation.membership.b2bcommission;

        dmc.save(function (err, saved) {
            err != null ? console.log(err) : null;
            console.log('dmc : ' + dmc.code);
            console.log('dmc b2b: ' + dmc.membership.b2cchannel);
            console.log('dmc b2b comission: ' + dmc.membership.b2bcommission);
            console.log('saved!');
            totals.activated++;
            cev.emit('dmc.pop');
        });

    });

    cev.on('dmc.next', function (dmc) {
        core.list('dmcs').model.find({ code : dmc.code })
        .exec(function (err, doc) { 
            (doc != null && doc.length > 0) ? cev.emit('dmc.process', dmc, doc[0]) : cev.emit('dmc.pop');
        });
    });

    cev.on('dmc.pop', function () {
        if (activation != null && activation.length > 0) {
            var currentdmc = activation.shift();
            console.log('lets process ' + currentdmc.code);
            cev.emit('dmc.next', currentdmc);
        } else {
            cev.emit('activation.done');
        }

    });

    cev.emit('dmc.pop');
}
